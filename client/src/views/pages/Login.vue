<template>
  <div class="app flex-row align-items-center">
    <div class="container">
      <b-row class="justify-content-center">
        <b-col md="6">
          <b-card-group>
            <b-card no-body class="p-4">
              <b-card-body>
                <b-form v-on:submit.prevent="onSubmit" >
                  <h1>Login</h1>
                  <p class="text-muted">Sign In to your account</p>
                  <b-input-group class="mb-3">
                    <b-input-group-prepend><b-input-group-text><i class="icon-user"></i></b-input-group-text></b-input-group-prepend>
                    <b-form-input type="text" class="form-control" placeholder="Username" autocomplete="username email" v-model="username" />
                  </b-input-group>
                  <b-input-group class="mb-4">
                    <b-input-group-prepend><b-input-group-text><i class="icon-lock"></i></b-input-group-text></b-input-group-prepend>
                    <b-form-input type="password" class="form-control" placeholder="Password" autocomplete="current-password" />
                  </b-input-group>
                  <b-row>
                      <b-button type='submit' variant="primary" class="px-4">Login</b-button>
                      <b-button variant="link" class="px-0">Forgot password?</b-button>
                  </b-row>
                </b-form>
              </b-card-body>
            </b-card>
          </b-card-group>
        </b-col>
      </b-row>
    </div>
  </div>
</template>

<script>
export default {
  name: 'Login',
 data: () => {
    return {
      username: null
    };
  },
  methods: {
    onSubmit() {      
      let user = this.username || 'anonymous';
      let role = this.username == 'jmarston' ? 'admin' : 'basic';
      localStorage.setItem('username', user);
      localStorage.setItem('role', role);
      this.$router.push({ path: '/' });
    }
  }
}
</script>
